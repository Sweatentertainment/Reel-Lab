#!/usr/bin/env node
/**
 * Reads public/images/characters/*.jpg and public/videos/characters/*.mp4
 * and writes src/lib/assets.generated.ts (sorted, with video links by id prefix).
 *
 * Excludes site marketing stills that may also exist under characters/ output.
 * Run after: bash scripts/optimize-assets.sh
 */
import { readdirSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const CHAR_IMG = join(ROOT, "public/images/characters");
const CHAR_VID = join(ROOT, "public/videos/characters");
const OUT = join(ROOT, "src/lib/assets.generated.ts");

/** Display names keyed by JPEG filename (e.g. 29_dusty_ragdoll.jpg → RAGDOLL). */
const NAME_BY_JPEG = {
  "05_helm_rider.jpg": "HELM",
  "06_mori_forest.jpg": "MORI",
  "09_reef_deep_sea.jpg": "REEF",
  "11_static_tvhead.jpg": "STATIC",
  "12_orchid_alien.jpg": "ORCHID",
  "14_ember_fire.jpg": "EMBER",
  "19_pollen_mushroom.jpg": "POLLEN",
  "22_halo_angel.jpg": "HALO",
  "23_koi_water.jpg": "KOI",
  "25_zephyr_cloud.jpg": "ZEPHYR",
  "26_gumm_gummy.jpg": "GUMM",
  "27_biscuit_dog.jpg": "BISCUIT",
  "28_chomp_dino.jpg": "CHOMP",
  "29_dusty_bunny.jpg": "DUSTY",
  "29_dusty_ragdoll.jpg": "RAGDOLL",
  "30_sock_puppet.jpg": "SOCK",
  "36_clay_sculpture.jpg": "CLAY",
  "salt_flat.jpg": "SALT",
};

const SITE_STILLS = new Set([
  "01_hero_dj.jpg",
  "02_lifestyle_swim.jpg",
  "03_destination_como.jpg",
]);

function listSafe(dir, ext) {
  try {
    return readdirSync(dir).filter((f) => f.toLowerCase().endsWith(ext));
  } catch {
    return [];
  }
}

function displayName(jpegFile) {
  if (NAME_BY_JPEG[jpegFile]) return NAME_BY_JPEG[jpegFile];
  const base = jpegFile.replace(/\.jpg$/i, "");
  const m = base.match(/^\d+_(.+)$/);
  if (!m) return base.split("_")[0].toUpperCase();
  const first = m[1].split("_")[0];
  return first.toUpperCase();
}

function numericSortKey(filename) {
  const m = filename.match(/^(\d+)_/);
  return m ? parseInt(m[1], 10) : 9999;
}

function findVideoForJpg(jpegFile, mp4s) {
  const stem = jpegFile.replace(/\.jpg$/i, "");
  const m = stem.match(/^(\d+)_(.+)$/);
  if (!m) return undefined;
  const num = m[1];
  const tail = m[2];
  const firstWord = tail.split("_")[0];
  const exact = `vid_${num}_${firstWord}.mp4`;
  if (mp4s.includes(exact)) return `/videos/characters/${exact}`;
  const loose = mp4s.find((v) => v.startsWith(`vid_${num}_`));
  return loose ? `/videos/characters/${loose}` : undefined;
}

const jpgs = listSafe(CHAR_IMG, ".jpg").filter((f) => !SITE_STILLS.has(f));
const mp4s = listSafe(CHAR_VID, ".mp4");

jpgs.sort((a, b) => {
  const na = numericSortKey(a);
  const nb = numericSortKey(b);
  if (na !== nb) return na - nb;
  return a.localeCompare(b);
});

const entries = jpgs.map((jpg) => {
  const name = displayName(jpg);
  const image = `/images/characters/${jpg}`;
  const video = findVideoForJpg(jpg, mp4s);
  const videoLine = video ? `, video: "${video}"` : "";
  return `  { name: "${name}", image: "${image}"${videoLine} },`;
});

const header = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/build-assets-manifest.mjs — do not edit by hand.
 * Source: ls public/images/characters/*.jpg + public/videos/characters/*.mp4
 * Re-run: npm run optimize:assets
 */
export type CharacterEntry = {
  name: string;
  image: string;
  video?: string;
};

export const generatedCharacters: CharacterEntry[] = [
`;

const footer = `];
`;

const body = entries.length ? `${entries.join("\n")}\n` : "";

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, `${header}${body}${footer}`, "utf8");

console.log(
  `Wrote ${OUT} (${entries.length} character stills, ${mp4s.length} videos on disk).`,
);
if (entries.length === 0) {
  console.warn(
    "No character JPGs found under public/images/characters/. Run optimize:assets after adding PNGs to /content.",
  );
}
